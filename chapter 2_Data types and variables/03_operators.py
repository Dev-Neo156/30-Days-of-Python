# Arithmetic operators
a = 19
b =6

print(a + b)
print(a - b)
print(a * b)
print(a / b)

# Assignment operators
x = 5
print(x)
x += 3
print(x)
x-= 2
print(x)

# Comparison operators
print(a == b)
print(a!= b)
print(a> b)
print(a< b)
print(a >= b)
print(a <= b)

#logical operators
e = True or False # if either of the operands is true, then the condition becomes true.
print(e)
e = True and False # if both the operands are true, then the condition becomes true.
print(e)
e = False # if the condition is true, then the result becomes false and vice versa.
print(not(e))